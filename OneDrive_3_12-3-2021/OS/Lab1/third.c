#include<stdio.h>
#include<stdlib.h>
 
int main()
{
    int a=100;
    float b=100.99;
    char c='a';
    char str[]="Rhea Adhikari";
    printf("Integer : a=%d\n",a);
    printf("Float : b=%f\n",b);
    printf("Character : c=%c\n",c);
    printf("String : str=%s\n",str);
    printf("Scientific Notation: %e\n", b);
    printf("Octal for 100: %o\n", a);
    printf("Hexadecimal for 100 : %x\n",a);
}
